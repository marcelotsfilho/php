<?php

/*
    Em PHP existem diversos operadores aritméticos que podem ser utilizados

    soma (+)
    subtração (-)
    multiplicação (*)
    divisão (/)
    módulo (%)
    exponenciação (**)
    incremento (++)
    decremento (--)

*/

$number1 = 10;
$number2 = 2;

echo 'Soma: ' . ($number1 + $number2). '</br>';
echo 'Subtração: ' . ($number1 - $number2). '</br>';
echo 'Multiplicação: ' . ($number1 * $number2). '</br>';
echo 'Divisão: ' . ($number1 / $number2). '</br>';
echo 'Módulo: ' . ($number1 % $number2). '</br>';
echo 'Exponenciação: ' . ($number1 ** $number2). '</br>';

// O incremento e decremento podem ser feitos em condicionais